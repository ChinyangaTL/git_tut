// git init - create a new repository
// git status - view the changes to your project code
// git add <filename> - add all files to the staging area
// git add . - add all files to the staging area
// git commit -m "Initial commit" - create a new commit with files from the staging area
// git log - view recent commits
// git log --online - view recent commits in a compact format
// git reset --soft HEAD^ - undo the last commit
// git commit --amend -m "<message>" - amend the last commit message
// git branch - view all branches
// git checkout -b <branchname> - create a new branch and switch to it (-b represents new branch)
// git checkout <branchname> - switch to a branch
// git branch -D <branchname> - delete a branch
// git branch -m <branchToChange> <newBranchName> - rename a branch
// git merge <branchname> - merge a branch into the current branch
